# Combine all tables into a dictionary
PIECE_TABLES = {
    chess.PAWN: PAWN_TABLE,
    chess.KNIGHT: KNIGHT_TABLE,
    chess.BISHOP: BISHOP_TABLE,
    chess.ROOK: ROOK_TABLE,
    chess.QUEEN: QUEEN_TABLE,
    chess.KING: KING_TABLE
}

def get_piece_value(piece_type):
    """Get the base value of a piece type."""
    return PIECE_VALUES.get(piece_type, 0)

def get_piece_square_value(piece, square):
    """Get the positional value of a piece on a given square."""
    if piece is None:
        return 0
    
    table = PIECE_TABLES.get(piece.piece_type)
    if table is None:
        return 0
    
    # Get the value from the table, flipping the board for black pieces
    if piece.color == chess.BLACK:
        square = chess.square_mirror(square)
    return table[square]

def get_piece_symbol(piece):
    """Get the Unicode symbol for a piece."""
    if piece is None:
        return " "
    
    symbols = {
        chess.PAWN: "♟" if piece.color == chess.BLACK else "♙",
        chess.KNIGHT: "♞" if piece.color == chess.BLACK else "♘",
        chess.BISHOP: "♝" if piece.color == chess.BLACK else "♗",
        chess.ROOK: "♜" if piece.color == chess.BLACK else "♖",
        chess.QUEEN: "♛" if piece.color == chess.BLACK else "♕",
        chess.KING: "♚" if piece.color == chess.BLACK else "♔"
    }
    return symbols.get(piece.piece_type, " ") 